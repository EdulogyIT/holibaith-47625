export const searchAlgerianCities = (query: string, lang: 'FR' | 'EN' | 'AR' = 'FR'): typeof algerianCities => {
  if (!query || query.trim().length === 0) return [];
  
  const searchTerm = query.toLowerCase().trim();
  
  return algerianCities.filter(city => {
    const nameToSearch = lang === 'AR' ? city.nameAr : (lang === 'FR' ? city.nameFr : city.name);
    return nameToSearch.toLowerCase().includes(searchTerm) || 
           city.name.toLowerCase().includes(searchTerm) ||
           city.nameFr.toLowerCase().includes(searchTerm);
  }).slice(0, 10); // Limit to 10 suggestions
};
